def update_counts_and_dates(html_text: str, total_posts: int, update_time: str, last_push_iso: str) -> str:
    html_text = re.sub(
        r'(<div class="headline">文章</div><div class="length-num">)(\d+)(</div>)',
        rf"\\1{total_posts}\\3",
        html_text,
    )
    html_text = re.sub(
        r'(<div class="item-name">文章数目 :</div><div class="item-count">)(\d+)(</div>)',
        rf"\\1{total_posts}\\3",
        html_text,
    )
    html_text = re.sub(
        r'(data-lastPushDate=")[^"]*(")',
        rf"\\1{last_push_iso}\\2",
        html_text,
    )
    html_text = re.sub(
        r"(postUpdate: ')([^']*)(')",
        rf"\\1{update_time}\\3",
        html_text,
    )
    return html_text


def update_post_page(
    template_text: str,
    new_title: str,
    desc: str,
    new_url: str,
    cover_rel: str,
    cover_abs: str,
    publish_iso: str,
    publish_title: str,
    publish_date: str,
    update_iso: str,
    update_title: str,
    update_date: str,
    update_time: str,
    article_html: str,
    prev_post_url: str,
    prev_post_title: str,
    prev_post_cover: str,
) -> str:
    text = template_text

    old_title = extract_first(r'<h1 class="post-title">([^<]+)</h1>', text)
    if old_title:
        text = text.replace(old_title, new_title)

    text = re.sub(
        r'(<title>)([^<]+)(</title>)',
        rf"\\1{new_title} | 可乐&雪碧 日常\\3",
        text,
        count=1,
    )
    text = re.sub(r'(meta name="description" content=")[^"]*(")', rf"\\1{desc}\\2", text, count=1)
    text = re.sub(r'(meta property="og:description" content=")[^"]*(")', rf"\\1{desc}\\2", text, count=1)
    text = re.sub(r'(meta property="og:title" content=")[^"]*(")', rf"\\1{new_title}\\2", text, count=1)
    text = re.sub(r'(meta property="og:url" content=")[^"]*(")', rf"\\1{new_url}\\2", text, count=1)
    text = re.sub(r'(<link rel="canonical" href=")[^"]*(")', rf"\\1{new_url}\\2", text, count=1)
    text = re.sub(r'(meta property="og:image" content=")[^"]*(")', rf"\\1{cover_abs}\\2", text, count=1)
    text = re.sub(r'(meta name="twitter:image" content=")[^"]*(")', rf"\\1{cover_abs}\\2", text, count=1)
    text = re.sub(r'(data-image=")[^"]*(")', rf"\\1{cover_abs}\\2", text, count=1)
    text = re.sub(
        r"(post-bg\"[^>]*background-image: url\(')([^']*)('\))",
        rf"\\1{cover_rel}\\3",
        text,
        count=1,
    )
    text = re.sub(r"(title: ')([^']*)(')", rf"\\1{new_title}\\3", text, count=1)

    text = re.sub(
        r'(post-meta-date-created" datetime=")[^"]*(" title="发表于 )[^"]*(">)([^<]*)',
        rf"\\1{publish_iso}\\2{publish_title}\\3{publish_date}",
        text,
        count=1,
    )
    text = re.sub(
        r'(post-meta-date-updated" datetime=")[^"]*(" title="更新于 )[^"]*(">)([^<]*)',
        rf"\\1{update_iso}\\2{update_title}\\3{update_date}",
        text,
        count=1,
    )
    text = re.sub(r'(data-flag-title=")[^"]*(")', rf"\\1{new_title}\\2", text, count=1)
    text = re.sub(r"(postUpdate: ')([^']*)(')", rf"\\1{update_time}\\3", text, count=1)

    # Replace article content
    start = text.find('<article class="post-content" id="article-container">')
    end = text.find("</article>", start)
    if start != -1 and end != -1:
        end += len("</article>")
        text = text[:start] + article_html + text[end:]

    # Replace pagination (latest post -> only next)
    pagination = (
        '<nav class="pagination-post" id="pagination">'
        '<div class="next-post pull-full">'
        f'<a href="{prev_post_url}" title="{prev_post_title}">'
        f'<img class="cover" src="{prev_post_cover}" onerror="onerror=null;src=\'{SITE_ROOT}img/404.jpg\'" alt="cover of next post">'
        '<div class="pagination-info"><div class="label">下一篇</div>'
        f'<div class="next_info">{prev_post_title}</div></div></a></div></nav>'
    )
    pag_start = text.find('<nav class="pagination-post"')
    pag_end = text.find("</nav>", pag_start)
    if pag_start != -1 and pag_end != -1:
        pag_end += len("</nav>")
        text = text[:pag_start] + pagination + text[pag_end:]

    return text


