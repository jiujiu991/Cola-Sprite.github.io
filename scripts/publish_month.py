def month_previous(today: dt.date) -> Tuple[int, int]:
    first = today.replace(day=1)
    prev_last = first - dt.timedelta(days=1)
    return prev_last.year, prev_last.month


def parse_month(month_str: Optional[str]) -> Tuple[int, int]:
    if not month_str:
        return month_previous(dt.date.today())
    m = re.fullmatch(r"(\d{4})-(\d{2})", month_str)
    if not m:
        raise ValueError("Month must be in YYYY-MM format.")
    return int(m.group(1)), int(m.group(2))


def parse_date(date_str: Optional[str]) -> dt.date:
    if not date_str:
        return dt.date.today()
    return dt.datetime.strptime(date_str, "%Y-%m-%d").date()


def list_images(src_dir: Path) -> List[Path]:
    if not src_dir.exists():
        return []
    images = []
    for p in src_dir.iterdir():
        if p.is_file() and p.suffix.lower() in IMAGE_EXTS:
            images.append(p)
    return sorted(images)


def month_date_range(year: int, month: int) -> Tuple[dt.date, dt.date]:
    start = dt.date(year, month, 1)
    if month == 12:
        end = dt.date(year + 1, 1, 1) - dt.timedelta(days=1)
    else:
        end = dt.date(year, month + 1, 1) - dt.timedelta(days=1)
    return start, end


def extract_uuids(data) -> List[str]:
    uuids = []
    if isinstance(data, dict):
        if isinstance(data.get("uuid"), str):
            uuids.append(data["uuid"])
        for value in data.values():
            uuids.extend(extract_uuids(value))
    elif isinstance(data, list):
        for item in data:
            uuids.extend(extract_uuids(item))
    return uuids


def clear_dir_images(dir_path: Path) -> None:
    if not dir_path.exists():
        return
    for p in dir_path.iterdir():
        if p.is_file() and p.suffix.lower() in IMAGE_EXTS:
            p.unlink()


def convert_images_to_jpeg(dir_path: Path) -> None:
    sips = shutil.which("sips")
    if not sips:
        return
    for p in dir_path.iterdir():
        if not p.is_file():
            continue
        if p.suffix.lower() in {".jpg", ".jpeg"}:
            continue
        if p.suffix.lower() not in IMAGE_EXTS:
            continue
        target = p.with_suffix(".jpg")
        if target.exists():
            target = p.with_name(f"{p.stem}-converted.jpg")
        subprocess.run(
            [sips, "-s", "format", "jpeg", str(p), "--out", str(target)],
            check=False,
            capture_output=True,
            text=True,
        )
        try:
            p.unlink()
        except FileNotFoundError:
            pass
        try:
            os.chmod(target, 0o644)
        except Exception:
            pass


def detect_photos_library(osxphotos: str) -> str:
    try:
        result = subprocess.run(
            [osxphotos, "list", "--json"],
            check=False,
            capture_output=True,
            text=True,
            timeout=30,
        )
    except Exception:
        return ""
    if not result.stdout:
        return ""
    try:
        data = json.loads(result.stdout)
    except Exception:
        return ""
    last_lib = data.get("last_library")
    if isinstance(last_lib, str) and last_lib:
        return last_lib
    libs = data.get("photo_libraries")
    if isinstance(libs, list) and libs:
        return libs[0]
    return ""


def export_random_photos_from_photos(
    year: int,
    month: int,
    dest_dir: Path,
    count: int,
    library_path: str = "",
) -> bool:
    osxphotos = shutil.which("osxphotos")
    if not osxphotos:
        try:
            user_base = subprocess.run(
                [sys.executable, "-m", "site", "--user-base"],
                check=False,
                capture_output=True,
                text=True,
            ).stdout.strip()
        except Exception:
            user_base = ""
        candidates = []
        if user_base:
            candidates.append(str(Path(user_base) / "bin" / "osxphotos"))
        candidates.append(str(Path.home() / ".local" / "bin" / "osxphotos"))
        candidates.append(str(OSXPHOTOS))
        for cand in candidates:
            if cand and Path(cand).exists():
                osxphotos = cand
                break
    if not osxphotos or not Path(osxphotos).exists():
        print("ERROR: osxphotos CLI not found. Install with `python3 -m pip install --user osxphotos`.")
        return False

    if not library_path:
        library_path = detect_photos_library(osxphotos)

    start, end = month_date_range(year, month)
    from_date = start.isoformat()
    to_date = end.isoformat()

    query_cmd = [
        osxphotos,
        "query",
        "--from-date",
        from_date,
        "--to-date",
        to_date,
        "--only-photos",
        "--json",
    ]
    if library_path:
        query_cmd.extend(["--library", library_path])
    try:
        result = subprocess.run(
            query_cmd,
            check=False,
            capture_output=True,
            text=True,
            timeout=120,
        )
    except Exception as exc:
        print(f"ERROR: osxphotos query failed: {exc}")
        return False
    if not result.stdout:
        err = (result.stderr or "").strip()
        if err:
            print(f"ERROR: osxphotos query error: {err}")
        else:
            print("ERROR: osxphotos query returned no data.")
        return False
    try:
        data = json.loads(result.stdout)
    except Exception as exc:
        print(f"ERROR: Failed to parse osxphotos JSON: {exc}")
        return False

    uuids = list(dict.fromkeys(extract_uuids(data)))
    if not uuids:
        print("ERROR: No photos found for the month in Photos.app.")
        return False

    count = max(1, count)
    pick = random.sample(uuids, min(count, len(uuids)))

    dest_dir.mkdir(parents=True, exist_ok=True)
    clear_dir_images(dest_dir)

    export_cmd = [osxphotos, "export", "--only-photos"]
    for uid in pick:
        export_cmd.extend(["--uuid", uid])
    if library_path:
        export_cmd.extend(["--library", library_path])
    export_cmd.append(str(dest_dir))
    try:
        subprocess.run(
            export_cmd,
            check=False,
            capture_output=True,
            text=True,
            timeout=300,
        )
    except Exception as exc:
        print(f"ERROR: osxphotos export failed: {exc}")
        return False

    # Remove osxphotos export db if created
    export_db = dest_dir / ".osxphotos_export.db"
    if export_db.exists():
        try:
            export_db.unlink()
        except Exception:
            pass

    convert_images_to_jpeg(dest_dir)
    return True


def image_size_sips(path: Path) -> Optional[Tuple[int, int]]:
    sips = shutil.which("sips")
    if not sips:
        return None
    try:
        result = subprocess.run(
            [sips, "-g", "pixelWidth", "-g", "pixelHeight", str(path)],
            check=False,
            capture_output=True,
            text=True,
            timeout=5,
        )
    except Exception:
        return None
    width = height = None
    for line in (result.stdout or "").splitlines():
        if "pixelWidth" in line:
            width = int(line.strip().split()[-1])
        if "pixelHeight" in line:
            height = int(line.strip().split()[-1])
    if width and height:
        return width, height
    return None


def cover_score(path: Path) -> float:
    name = path.stem.lower()
    score = 0.0
    if name.startswith("cover"):
        score += 1000.0
    if any(k in name for k in ["sky", "sunset", "sea", "mountain", "city", "street", "park"]):
        score += 50.0
    size = image_size_sips(path)
    if size:
        w, h = size
        ratio = w / h if h else 1.0
        # Favor landscape ratio
        if 1.2 <= ratio <= 2.4:
            score += 200.0
        score += (w * h) / 1_000_000.0
    else:
        # Fallback to file size
        score += path.stat().st_size / 1_000_000.0
    # Recent files slight bonus
    score += path.stat().st_mtime / 1_000_000_000.0
    return score


def pick_cover(images: List[Path]) -> Optional[Path]:
    if not images:
        return None
    return max(images, key=cover_score)


def ensure_copy_images(images: List[Path], dst_dir: Path, dry_run: bool) -> None:
    if dry_run:
        return
    dst_dir.mkdir(parents=True, exist_ok=True)
    for img in images:
        shutil.copy2(img, dst_dir / img.name)
        os.chmod(dst_dir / img.name, 0o644)


def is_valid_template(path: Path) -> bool:
    try:
        text = path.read_text(encoding="utf-8")
    except Exception:
        return False
    if "<title>" not in text or "</title>" not in text:
        return False
    return True


def find_latest_post() -> Path:
    posts = []
    for p in REPO_ROOT.rglob("index.html"):
        rel = p.relative_to(REPO_ROOT).as_posix()
        m = re.match(r"(\d{4})/(\d{2})/(\d{2})/[^/]+/index.html$", rel)
        if not m:
            continue
        y, mo, d = map(int, m.groups())
        if is_valid_template(p):
            posts.append((dt.date(y, mo, d), p))
    if not posts:
        raise RuntimeError("No existing posts found to use as template.")
    posts.sort(key=lambda x: x[0], reverse=True)
    return posts[0][1]


def post_link_from_path(path: Path) -> str:
    rel = path.relative_to(REPO_ROOT).as_posix()
    m = re.match(r"(\d{4})/(\d{2})/(\d{2})/([^/]+)/index.html$", rel)
    if not m:
        return ""
    y, mo, d, slug = m.groups()
    return f"{SITE_ROOT}{y}/{mo}/{d}/{slug}/"


def extract_first(pattern: str, text: str, default: str = "") -> str:
    m = re.search(pattern, text, re.DOTALL)
    return m.group(1) if m else default


def openclaw_generate(prompt: str, openclaw_path: str) -> Optional[Dict]:
    if not Path(openclaw_path).exists():
        return None
    try:
        result = subprocess.run(
            [openclaw_path, "agent", "--local", "--message", prompt],
            check=False,
            capture_output=True,
            text=True,
            timeout=600,
        )
    except Exception:
        return None

    output = (result.stdout or "").strip()
    if not output:
        return None

    # Try to extract JSON from output
    start = output.find("{")
    end = output.rfind("}")
    if start == -1 or end == -1 or end <= start:
        return None
    try:
        return json.loads(output[start : end + 1])
    except Exception:
        return None


def encode_image_base64(image_path: Path) -> str:
    """Encode image to base64 string."""
    try:
        with open(image_path, "rb") as f:
            return base64.b64encode(f.read()).decode("utf-8")
    except Exception:
        return ""


def analyze_images(image_paths: List[Path], openclaw_path: str) -> str:
    """Use vision model to analyze images and return descriptions."""
    if not image_paths or not Path(openclaw_path).exists():
        return ""

    # Check if openclaw agent supports --image
    try:
        help_out = subprocess.run(
            [openclaw_path, "agent", "--help"],
            check=False,
            capture_output=True,
            text=True,
            timeout=5,
        )
        if "--image" not in (help_out.stdout or ""):
            return ""
    except Exception:
        return ""
    
    # Only analyze first 4 images to keep prompt size reasonable
    images_to_analyze = image_paths[:4]
    
    descriptions = []
    for img in images_to_analyze:
        b64 = encode_image_base64(img)
        if not b64:
            continue
        
        # Use openclaw agent with vision to describe the image
        vision_prompt = f"describe this image in one short Chinese sentence (within 20 words). Focus on: what is in the image, the mood/vibe, colors. Just describe, don't interpret. Image file: {img.name}"
        
        try:
            result = subprocess.run(
                [openclaw_path, "agent", "--local", "--message", vision_prompt, "--image", f"data:image/jpeg;base64,{b64}"],
                check=False,
                capture_output=True,
                text=True,
                timeout=60,
            )
            desc = (result.stdout or "").strip()
            if desc and len(desc) < 200:
                descriptions.append(f"- {img.name}: {desc}")
        except Exception:
            continue
    
    if descriptions:
        return "\n图片内容分析：\n" + "\n".join(descriptions)
    return ""


