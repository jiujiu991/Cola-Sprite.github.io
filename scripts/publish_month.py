def update_archives_index(
    html_text: str,
    year: int,
    new_item: str,
) -> str:
    year_marker = f'<div class="article-sort-item year">{year}</div>'
    if year_marker in html_text:
        return html_text.replace(year_marker, year_marker + new_item, 1)

    # Insert new year at the top of article-sort
    insert_point = '<div class="article-sort">'
    if insert_point in html_text:
        return html_text.replace(insert_point, insert_point + year_marker + new_item, 1)
    return html_text


def update_archive_list(html_text: str, month_link: str, month_label: str, count: int) -> str:
    if month_link in html_text:
        return html_text
    item = (
        '<li class="card-archive-list-item">'
        f'<a class="card-archive-list-link" href="{month_link}">'
        f'<span class="card-archive-list-date">{month_label}</span>'
        f'<span class="card-archive-list-count">{count}</span>'
        "</a></li>"
    )
    anchor = '<ul class="card-archive-list">'
    if anchor in html_text:
        return html_text.replace(anchor, anchor + item, 1)
    return html_text


def update_search_xml(search_text: str, title: str, link: str, url: str, article_html: str) -> str:
    if f"<url>{url}</url>" in search_text:
        return search_text
    entry = (
        "\n    <entry>\n"
        f"      <title>{title}</title>\n"
        f"      <link href=\"{link}\"/>\n"
        f"      <url>{url}</url>\n"
        "      \n"
        "        <content type=\"html\"><![CDATA["
        "<link rel=\"stylesheet\" class=\"aplayer-secondary-style-marker\" href=\"/Cola-Sprite.github.io/assets/css/APlayer.min.css\">"
        "<script src=\"/Cola-Sprite.github.io/assets/js/APlayer.min.js\" class=\"aplayer-secondary-script-marker\"></script>"
        f"{article_html}]]></content>\n"
        "      \n"
        "      \n"
        "      \n"
        "    </entry>\n"
    )
    insert_point = "<search>"
    idx = search_text.find(insert_point)
    if idx == -1:
        return search_text
    insert_at = idx + len(insert_point)
    return search_text[:insert_at] + entry + search_text[insert_at:]


def main() -> int:
    args = parse_args()

    year, month = parse_month(args.month)
    publish_date = parse_date(args.date)
    month_str = f"{month:02d}"
    month_cn = CN_MONTHS[month - 1]

    src_dir = SOURCE_BASE / f"{year}-{month_str}"
    images = list_images(src_dir)
    if not images:
        print(f"ERROR: No images found in {src_dir}")
        return 1

    cover = pick_cover(images)
    if cover is None:
        print("ERROR: Could not pick a cover image.")
        return 1

    ensure_copy_images(images, REPO_ROOT / "img" / f"{year}-{month_str}", args.dry_run)

    title = f"{year}年{month_str} Daily！"
    desc_default = f"{month_cn}：日常与小确幸～"

    prompt = build_prompt(year, month_cn, title, [p.name for p in images])
    content = None if args.no_openclaw else openclaw_generate(prompt, args.openclaw_path)
    if not content:
        content = fallback_content(year, month_cn)

    # Build article
    img_rel_paths = [
        f"{SITE_ROOT}img/{year}-{month_str}/{p.name}"
        for p in images
        if p.name != cover.name
    ]
    if not img_rel_paths:
        img_rel_paths = [f"{SITE_ROOT}img/{year}-{month_str}/{cover.name}"]

    article_html = build_article_html(content, img_rel_paths)

    # New post path
    slug = f"{year}{month_str}"
    post_dir = REPO_ROOT / str(year) / month_str / f"{publish_date.day:02d}" / slug
    post_path = post_dir / "index.html"
    if post_path.exists():
        print(f"ERROR: Post already exists: {post_path}")
        return 1

    template_path = find_latest_post()
    template_text = template_path.read_text(encoding="utf-8")

    cover_rel = f"{SITE_ROOT}img/{year}-{month_str}/{cover.name}"
    cover_abs = f"https://jiujiu991.github.io{cover_rel}"
    post_url = f"https://jiujiu991.github.io{SITE_ROOT}{year}/{month_str}/{publish_date.day:02d}/{slug}/index.html"
    post_link = f"{SITE_ROOT}{year}/{month_str}/{publish_date.day:02d}/{slug}/"

    publish_iso = f"{publish_date.isoformat()}T00:00:00.000Z"
    update_iso = f"{publish_date.isoformat()}T12:00:00.000Z"
    publish_title = f"发表于 {publish_date} 08:00:00"
    update_title = f"更新于 {publish_date} 20:00:00"
    publish_date_str = publish_date.isoformat()
    update_date_str = publish_date.isoformat()
    update_time = f"{publish_date} 20:00:00"
    last_push_iso = f"{publish_date.isoformat()}T12:00:00.000Z"

    prev_title = extract_first(r'<h1 class="post-title">([^<]+)</h1>', template_text, "上一篇")
    prev_url = extract_first(r'<link rel="canonical" href="([^"]+)"', template_text, "")
    prev_link = prev_url.replace("https://jiujiu991.github.io", "") if prev_url else post_link_from_path(template_path)
    prev_cover = extract_first(r'meta property="og:image" content="([^"]+)"', template_text, cover_abs)

    updated_post = update_post_page(
        template_text=template_text,
        new_title=title,
        desc=content.get("h1", desc_default),
        new_url=post_url,
        cover_rel=cover_rel,
        cover_abs=cover_abs,
        publish_iso=publish_iso,
        publish_title=publish_title,
        publish_date=publish_date_str,
        update_iso=update_iso,
        update_title=update_title,
        update_date=update_date_str,
        update_time=update_time,
        article_html=article_html,
        prev_post_url=prev_link,
        prev_post_title=prev_title,
        prev_post_cover=prev_cover,
    )

    if not args.dry_run:
        post_dir.mkdir(parents=True, exist_ok=True)
        post_path.write_text(updated_post, encoding="utf-8")

    # Update index.html
    index_path = REPO_ROOT / "index.html"
    if index_path.exists():
        index_text = index_path.read_text(encoding="utf-8")
        new_item = build_recent_post_item(
            link=post_link,
            title=title,
            cover=cover_rel,
            publish_iso=publish_iso,
            publish_title=publish_title,
            publish_date=publish_date_str,
            excerpt=content.get("h1", desc_default),
        )
        anchor = '<div class="recent-posts" id="recent-posts">'
        if anchor in index_text:
            index_text = index_text.replace(anchor, anchor + new_item, 1)
        index_path.write_text(index_text, encoding="utf-8")

    # Update archives index
    archives_index = REPO_ROOT / "archives" / "index.html"
    if archives_index.exists():
        arch_text = archives_index.read_text(encoding="utf-8")
        arch_item = build_archive_item(
            link=post_link,
            title=title,
            cover=cover_rel,
            publish_iso=publish_iso,
            publish_title=publish_title,
            publish_date=publish_date_str,
        )
        arch_text = update_archives_index(arch_text, year, arch_item)
        arch_text = update_archive_list(
            arch_text,
            month_link=f"{SITE_ROOT}archives/{year}/{month_str}/",
            month_label=f"{month_cn} {year}",
            count=1,
        )
        archives_index.write_text(arch_text, encoding="utf-8")

    # Create/update monthly archive page
    month_archive_path = REPO_ROOT / "archives" / str(year) / month_str / "index.html"
    if month_archive_path.exists():
        month_text = month_archive_path.read_text(encoding="utf-8")
        month_item = build_archive_item(
            link=post_link,
            title=title,
            cover=cover_rel,
            publish_iso=publish_iso,
            publish_title=publish_title,
            publish_date=publish_date_str,
        )
        month_text = update_archives_index(month_text, year, month_item)
        month_text = re.sub(r'(文章总览 - )\d+', r"\\g<1>1", month_text)
        month_archive_path.write_text(month_text, encoding="utf-8")
    else:
        # Use any existing archive month as template
        template_arch = next(REPO_ROOT.glob("archives/*/*/index.html"))
        month_text = template_arch.read_text(encoding="utf-8")
        month_text = month_text.replace(
            extract_first(r"<title>([^<]+)</title>", month_text, ""),
            f"{month_cn} {year} | 可乐&雪碧 日常",
        )
        month_text = month_text.replace(
            extract_first(r'meta property="og:title" content="([^"]+)"', month_text, ""),
            f"{month_cn} {year}",
        )
        month_text = month_text.replace(
            extract_first(r'meta property="og:url" content="([^"]+)"', month_text, ""),
            f"https://jiujiu991.github.io{SITE_ROOT}archives/{year}/{month_str}/index.html",
        )
        month_text = month_text.replace(
            extract_first(r'<link rel="canonical" href="([^"]+)"', month_text, ""),
            f"https://jiujiu991.github.io{SITE_ROOT}archives/{year}/{month_str}/index.html",
        )
        month_item = build_archive_item(
            link=post_link,
            title=title,
            cover=cover_rel,
            publish_iso=publish_iso,
            publish_title=publish_title,
            publish_date=publish_date_str,
        )
        month_block = f'<div class="article-sort"><div class="article-sort-item year">{year}</div>{month_item}</div>'
        month_text = re.sub(r'<div class="article-sort">.*?</nav>', month_block + '<nav', month_text, flags=re.DOTALL)
        month_text = re.sub(r'(文章总览 - )\d+', r"\\g<1>1", month_text)
        if not args.dry_run:
            month_archive_path.parent.mkdir(parents=True, exist_ok=True)
            month_archive_path.write_text(month_text, encoding="utf-8")

    # Update search.xml
    search_path = REPO_ROOT / "search.xml"
    if search_path.exists():
        search_text = search_path.read_text(encoding="utf-8")
        search_text = update_search_xml(search_text, title, post_link, post_link, article_html)
        search_path.write_text(search_text, encoding="utf-8")

    # Update counts + timestamps across key pages
    if search_path.exists():
        total_posts = search_path.read_text(encoding="utf-8").count("<entry>")
    else:
        total_posts = 0

    # Update counts + timestamps across all html pages
    for p in REPO_ROOT.rglob("*.html"):
        html = p.read_text(encoding="utf-8")
        html = update_counts_and_dates(html, total_posts, update_time, last_push_iso)
        # Ensure archive list has the current month
        html = update_archive_list(
            html,
            month_link=f"{SITE_ROOT}archives/{year}/{month_str}/",
            month_label=f"{month_cn} {year}",
            count=1,
        )
        # Update archives index total
        if p.name == "index.html" and p.parent.name == "archives":
            html = re.sub(r'(文章总览 - )\d+', rf"\\g<1>{total_posts}", html)
        p.write_text(html, encoding="utf-8")

    print(f"OK: Generated {post_path}")
    print(f"Cover: {cover_rel}")
    if args.push and not args.dry_run:
        commit_msg = args.commit_message or f"Add {title}"
        subprocess.run(["git", "add", "-A"], cwd=str(REPO_ROOT), check=False)
        subprocess.run(["git", "commit", "-m", commit_msg], cwd=str(REPO_ROOT), check=False)
        subprocess.run(["git", "push"], cwd=str(REPO_ROOT), check=False)
        print("Git push: done.")
    else:
        print("Next steps:")
        print(f"  git add -A && git commit -m \"Add {title}\" && git push")
    return 0


