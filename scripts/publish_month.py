def month_previous(today: dt.date) -> Tuple[int, int]:
    first = today.replace(day=1)
    prev_last = first - dt.timedelta(days=1)
    return prev_last.year, prev_last.month


def parse_month(month_str: Optional[str]) -> Tuple[int, int]:
    if not month_str:
        return month_previous(dt.date.today())
    m = re.fullmatch(r"(\d{4})-(\d{2})", month_str)
    if not m:
        raise ValueError("Month must be in YYYY-MM format.")
    return int(m.group(1)), int(m.group(2))


def parse_date(date_str: Optional[str]) -> dt.date:
    if not date_str:
        return dt.date.today()
    return dt.datetime.strptime(date_str, "%Y-%m-%d").date()


def list_images(src_dir: Path) -> List[Path]:
    if not src_dir.exists():
        return []
    images = []
    for p in src_dir.iterdir():
        if p.is_file() and p.suffix.lower() in IMAGE_EXTS:
            images.append(p)
    return sorted(images)


def pick_cover(images: List[Path]) -> Optional[Path]:
    if not images:
        return None
    cover = [p for p in images if p.stem.lower().startswith("cover")]
    if cover:
        return sorted(cover)[0]
    return max(images, key=lambda p: p.stat().st_mtime)


def ensure_copy_images(images: List[Path], dst_dir: Path, dry_run: bool) -> None:
    if dry_run:
        return
    dst_dir.mkdir(parents=True, exist_ok=True)
    for img in images:
        shutil.copy2(img, dst_dir / img.name)
        os.chmod(dst_dir / img.name, 0o644)


def find_latest_post() -> Path:
    posts = []
    for p in REPO_ROOT.rglob("index.html"):
        rel = p.relative_to(REPO_ROOT).as_posix()
        m = re.match(r"(\d{4})/(\d{2})/(\d{2})/[^/]+/index.html$", rel)
        if not m:
            continue
        y, mo, d = map(int, m.groups())
        posts.append((dt.date(y, mo, d), p))
    if not posts:
        raise RuntimeError("No existing posts found to use as template.")
    posts.sort(key=lambda x: x[0], reverse=True)
    return posts[0][1]


def post_link_from_path(path: Path) -> str:
    rel = path.relative_to(REPO_ROOT).as_posix()
    m = re.match(r"(\d{4})/(\d{2})/(\d{2})/([^/]+)/index.html$", rel)
    if not m:
        return ""
    y, mo, d, slug = m.groups()
    return f"{SITE_ROOT}{y}/{mo}/{d}/{slug}/"


def extract_first(pattern: str, text: str, default: str = "") -> str:
    m = re.search(pattern, text, re.DOTALL)
    return m.group(1) if m else default


def openclaw_generate(prompt: str, openclaw_path: str) -> Optional[Dict]:
    if not Path(openclaw_path).exists():
        return None
    try:
        result = subprocess.run(
            [openclaw_path, "agent", "--local", "--message", prompt],
            check=False,
            capture_output=True,
            text=True,
            timeout=600,
        )
    except Exception:
        return None

    output = (result.stdout or "").strip()
    if not output:
        return None

    # Try to extract JSON from output
    start = output.find("{")
    end = output.rfind("}")
    if start == -1 or end == -1 or end <= start:
        return None
    try:
        return json.loads(output[start : end + 1])
    except Exception:
        return None


